export function getProjectBySlug(slug) {
  return projects.find((p) => p.slug === slug);
}

export function getAdjacentProjects(slug) {
  const index = projects.findIndex((p) => p.slug === slug);
  return {
    prev: index > 0 ? projects[index - 1] : projects[projects.length - 1],
    next: index < projects.length - 1 ? projects[index + 1] : projects[0],
  };
}

export function getProjectPath(slug) {
  if (documentaryPhotographySlugSet.has(slug)) {
    return `/documentaries/photography/${slug}`;
  }

  if (documentaryVideographySlugSet.has(slug)) {
    return `/documentaries/videography/${slug}`;
  }

  if (corporatePhotographySlugSet.has(slug)) {
    return `/corporate/photography/${slug}`;
  }

  if (corporateVideographySlugSet.has(slug)) {
    return `/corporate/videography/${slug}`;
  }

  return `/portfolio/${slug}`;
}
